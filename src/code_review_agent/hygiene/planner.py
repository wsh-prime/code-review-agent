def build_move_suggestions_from_semantic(
    repo_path: Path | str,
    semantic_classifications: list[SemanticClassification],
    *,
    confidence_threshold: float = 0.6,
) -> list[MoveSuggestion]:
    """Build move suggestions from LLM semantic classifications.

    Only generates suggestions for:
    - ``artifact_type`` in :data:`~code_review_agent.hygiene.taxonomy.MOVE_TYPES`.
    - ``confidence >= confidence_threshold``.
    - ``suggested_action != 'needs_human_review'``.
    """
    root = Path(repo_path)
    used_targets: set[str] = {sc.path for sc in semantic_classifications}
    suggestions: list[MoveSuggestion] = []

    for sc in semantic_classifications:
        if sc.artifact_type not in MOVE_TYPES:
            continue
        if sc.confidence < confidence_threshold:
            continue
        if sc.suggested_action == "needs_human_review":
            continue

        target_dir = SEMANTIC_TARGET_DIRS.get(sc.artifact_type)
        if not target_dir:
            continue

        source = Path(sc.path)
        suggested = Path(target_dir) / source.name
        if source.as_posix() == suggested.as_posix():
            continue

        safe_target = _deduplicate_target(root, suggested, used_targets)
        used_targets.add(safe_target)
        suggestions.append(
            MoveSuggestion(
                source_path=sc.path,
                suggested_path=safe_target,
                category=sc.artifact_type,
                reason=sc.reason,
                confidence=sc.confidence,
            )
        )

    return sorted(suggestions, key=lambda s: s.source_path)


def build_uncertain_queue(
    semantic_classifications: list[SemanticClassification],
) -> list[SemanticClassification]:
    """Return all files that need human review (uncertain or obsolete)."""
    return [
        sc for sc in semantic_classifications
        if sc.suggested_action == "needs_human_review"
    ]
